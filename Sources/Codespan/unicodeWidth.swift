/// Returns the [UAX #11](https://www.unicode.org/reports/tr11/) based width of `c`, or
/// `None` if `c` is a control character.
/// Ambiguous width characters are treated as narrow.
func unicodeWidth(of scalar: UnicodeScalar) -> UInt {
    let value = scalar.value

    if value < 0x7F {
        if value >= 0x20 {
            // U+0020 to U+007F (exclusive) are single-width ASCII codepoints
            return 1
        } else {
            // U+0000 to U+0020 (exclusive) are control codes
            return 0
        }
    }

    if value >= 0xA0 {
        // No characters >= U+00A0 are control codes, so we can consult the lookup tables
        return UInt(lookupWidth(value))
    }

    // U+007F to U+00A0 (exclusive) are control codes
    return 0
}

/// Returns the [UAX #11](https://www.unicode.org/reports/tr11/) based width of `value`
/// by consulting a multi-level lookup table.
///
private func lookupWidth(_ value: UInt32) -> UInt8 {
    let t1Offset = widthRoot[Int(value >> 13)]

    // Each sub-table in WIDTH_MIDDLE is 7 bits, and each stored entry is a byte,
    // so each sub-table is 128 bytes in size.
    // (Sub-tables are selected using the computed offset from the previous table.)
    let t2Offset = widthMiddle[Int(t1Offset)][Int((value >> 7) & 0x3F)]

    // Each sub-table in WIDTH_LEAVES is 6 bits, but each stored entry is 2 bits.
    // This is accomplished by packing four stored entries into one byte.
    // So each sub-table is 2**(7-2) == 32 bytes in size.
    // Since this is the last table, each entry represents an encoded width.
    let packedWidths = widthLeaves[Int(t2Offset)][Int((value >> 2) & 0x1F)]

    // Extract the packed width
    let shift = Int(2 * (value & 0b11))
    let width = (packedWidths >> shift) & 0b11

    if width < 3 {
        return width
    }

    switch value {
    case 0x000A:
        return 1
    case 0x05DC:
        return 1
    case 0x0622...0x0882:
        return 1
    case 0x1780...0x17AF:
        return 1
    case 0x17D8:
        return 3
    case 0x1A10:
        return 1
    case 0x2D31...0x2D6F:
        return 1
    case 0xA4FC...0xA4FD:
        return 1
    case 0xFE01, 0xFE0E, 0xFE0F:
        return 0
    case 0x10C03:
        return 1
    case 0x16D67, 0x16D68:
        return 1
    case 0x1F1E6...0x1F1FF:
        return 1
    case 0x1F3FB...0x1F3FF:
        return 2
    default:
        return 2
    }
}

